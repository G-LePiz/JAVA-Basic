package week02;

public class W03 {
    public static void main(String[] args) {
        System.out.println(10 > 9); // 10이 9보다 크냐? : 응!(True)
        System.out.println(10 >= 9); // 10이 9보다 크거나 같니? : 응!(True)
        System.out.println(10 < 9); // 10이 9보다 작니? : 아니!(False)
        System.out.println(10 <= 9); // 10이 9보다 작거나 같니? : 아니!
        System.out.println(10 == 10); // 10이 10이랑 같니? : 응!
        System.out.println(10 == 9); // 10이 9랑 같니? : 아니!
        System.out.println(10 != 10); // 10이 10이 아니니? : 아니!
        System.out.println(10 != 9); // 10이 9가 아니니? : 응!
    }
}
