package week02.Operator;

public class Practice {
    public static void main(String[] args) {
        // 구구단 생성기

        for (int i = 1; i <= 9; i++) {
            for (int j = 1; i <= 9; i++) {
                System.out.println(i + "곱하기" + j + "는" + (i * j) + "입니다");
            }
        }
    }
}
